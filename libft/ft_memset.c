#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*s;

	i = 0;
	s = (unsigned char *)b;
	while (i < len)
	{
		s[i] = (unsigned char)c;
		i++;
	}
	return (b);
}
int main ()
{
	char	s[] = "hellohfakshfadshfdsghkfg";
	int i = 2004;
	
	printf("%s", ft_memset(s, i, 10));
}